// class HexString
void HexString::bin2asc(const char *in, int in_len, char *out, int out_buf_len, int &out_len)
{
	if (in_len <= 0) {
		out_len = 0;
		return;
	}

	if (out_buf_len < 2 * in_len) {
		out_len = 0;
		return;
	}

	//
	out_len = 2 * in_len;
	int out_index = out_len - 1;

	for (int i = in_len - 1; i >=0 ; i--)
	{
		unsigned char c = (unsigned char)in[i];

		out[out_index - 1] = asc_map[c][0];
		out[out_index] = asc_map[c][1];

		out_index -= 2;
	}

	assert(out_index == -1);
	return;
}

void HexString::asc2bin(const char *in, int in_len, char *out, int out_buf_len, int &out_len)
{
	if (in_len <= 0) {
		out_len = 0;
		return;
	}

	if (0 != (in_len % 2)) {
		out_len = 0;
		return;
	}

	out_len = in_len / 2;

	if (out_buf_len < out_len) {
		out_len = 0;
		return;
	}

	//
	int in_index = 0;
	for (int i = 0; i < out_len; i++)
	{
		unsigned char h = bin_map[(unsigned char)in[in_index]];
		unsigned char l = bin_map[(unsigned char)in[in_index + 1]];

		if ((h == 0xff) || (l == 0xff))
		{
			out_len = 0;
			return;
		}

		unsigned c = (h << 4) | l;
		out[i] = c;

		in_index += 2;
	}

	return;
}


